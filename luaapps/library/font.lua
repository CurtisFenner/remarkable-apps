local function rasterizeGlyph(glyph, width, height, baseline)
	local colors = {}
	local minX = width
	local maxX = 1
	for y = 1, height do
		local n = glyph[y]
		for x = 1, width do
			local b = n % 2
			if b == 1 then
				minX = math.min(minX, x)
				maxX = math.max(maxX, x)
			end
			table.insert(colors, b == 1)
			n = n // 2
		end
	end

	return {
		width = width,
		height = height,
		left = minX,
		right = maxX,
		baseline = baseline,
		colors = colors,
	}
end

local function rasterizeFontData(fontData)
	local glyphs = {}
	for k, v in pairs(fontData.glyphs) do
		glyphs[k] = rasterizeGlyph(v, fontData.width, fontData.height, fontData.baseline)
	end
	return {
		glyphs = glyphs,
	}
end

